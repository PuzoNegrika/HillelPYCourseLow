x = int(input("Введіть 5-ти значне число на клавіатурі: "))
n1 = x % 10             # 12345 % 10 = 5
n2 = x // 10 % 10         # 12345 // 10 = 1234. 1234 % 10 = 4
n3 = x // 100 % 10        # 12345 // 10 = 123. 123 % 10 = 3
n4 = x // 1000 % 10       # 12345 // 1000 = 12. 12 % 10 = 2
n5 = x // 10000         # 12345 // 1000 = 1
reverse_numbers = n1 * 10000 + n2 * 1000 + n3 * 100 + n4 * 10 + n5
print(reverse_numbers)